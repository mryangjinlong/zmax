package com.zmaxfilm.model.socket;

import java.util.List;

/**
 * Created by jimmy on 2016/12/5.
 * 广告推送
 */
public class AdvertisingPush {
    private String action;
    private String id;
    private String title;
    private int effectTime;
    private int expiryTime;
    private List<ImgData> img;
    private List<VideoData> video;
    private String content;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getEffectTime() {
        return effectTime;
    }

    public void setEffectTime(int effectTime) {
        this.effectTime = effectTime;
    }

    public int getExpiryTime() {
        return expiryTime;
    }

    public void setExpiryTime(int expiryTime) {
        this.expiryTime = expiryTime;
    }

    public List<ImgData> getImg() {
        return img;
    }

    public void setImg(List<ImgData> img) {
        this.img = img;
    }

    public List<VideoData> getVideo() {
        return video;
    }

    public void setVideo(List<VideoData> video) {
        this.video = video;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
