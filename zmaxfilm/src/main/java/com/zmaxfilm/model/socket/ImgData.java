package com.zmaxfilm.model.socket;

/**
 * Created by jimmy on 2016/12/5.
 * 广告中的图片信息
 */
public class ImgData {

    private String url;     //下载url
    private String href;    //广告外链
    private int imgOrder;  //图片顺序
    private int imgTime;   //图片显示时间   单位秒
    private int imgLinkType;  //图片链接类型，暂时没用


    public int getImgOrder() {
        return imgOrder;
    }

    public void setImgOrder(int imgOrder) {
        this.imgOrder = imgOrder;
    }

    public int getImgTime() {
        return imgTime;
    }

    public void setImgTime(int imgTime) {
        this.imgTime = imgTime;
    }

    public int getImgLinkType() {
        return imgLinkType;
    }

    public void setImgLinkType(int imgLinkType) {
        this.imgLinkType = imgLinkType;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }
}
